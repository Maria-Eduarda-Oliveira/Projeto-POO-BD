package Model;
public class Tecnico {
    private int cpf;
    private String nome;
    private int idade;
    private String titulos;

    public Tecnico(int cpf, String nome, int idade, String titulos){
        this.cpf = cpf;
        this.nome = nome;
        this.idade = idade;
        this.titulos = titulos;
    }
}
